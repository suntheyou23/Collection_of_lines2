$(function() {
	$(".search").on("click", function() {
		Search_lines();
	});

	$(".num").keypress(function(e) {
		if (e.which == 13) {
			Search_lines();
			return false;
		}
	});

	$(".rand").on("click", function() {
		var rand = Math.floor( Math.random() * 1001 );
		$(".num").val(rand);
		Search_lines();
	});

	$(".copy").on("click", function() {
		Copy_line();
	});
});

$(window).on("load", function() {
	var height;
	var width = $(this).width();
	if(width <= 479){
		height = $("header").height() - 10;

	} else {
		height = $("header").height() - 30;
		$(".original_tytle").css("font-size", "15pt");
	}

	$(".original").width(height);
	$(".original").height(height);
	$(".original_tytle").width(height);
	$(".copy").hide();
});

function isNumber(val) {
	var regex = new RegExp(/^[0-9]+(\.[0-9]+)?$/);
	return regex.test(val);
}

function Search_lines() {
	var num = $(".num").val();
	var text = $(".num").text();
	if (isNumber(num) && (1 <= num && num <= 994)) {
		$(".line").text(lines_tab[num - 1].lines);
		$(".copy").show();
	} else {
		$(".line").text("1〜994の数値を入力してください。");
		$(".copy").hide();
	}
}

function Copy_line() {
	var line = $(".line").text();
	if(navigator.clipboard){
    navigator.clipboard.writeText(line);
	}
	alert("セリフをコピーしました。");
}
